    private void PR3_Compute_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PR3_Compute_BtnActionPerformed
        // TODO add your handling code here:
        int R, T;
        double P;

        try {
            P = Double.parseDouble(PR3_Input_InitialValue.getText());
            R = Integer.parseInt(PR3_Input_InterestRate.getText());
            T = Integer.parseInt(PR3_Input_TimeYear.getText());

            if (R > 100) {
                PR3_FutVal_Output.setText("Please follow the set Range given for Interest Rate.");
                PR3_IntEarn_Output.setText("");
                return;
            } else if (R < 1) {
                PR3_FutVal_Output.setText("Please follow the set Range given for Interest Rate.");
                PR3_IntEarn_Output.setText("");
                return;
            }
            if (N == 0) {
                PR3_FutVal_Output.setText("Please select a Number of Times Conpounded.");
                PR3_IntEarn_Output.setText("");
                return;
            }
            
            double A = P * Math.pow(1 + (R / 100.0) / N, N * T);
            double I = A - P;
                    
            PR3_FutVal_Output.setText(String.format("%.2f", A));
            PR3_IntEarn_Output.setText(String.format("%.2f", I));
        } catch (NumberFormatException ex) {
            PR3_FutVal_Output.setText("One or more Inputs are Invalid or Missing!");
            PR3_IntEarn_Output.setText("");
        }
    }//GEN-LAST:event_PR3_Compute_BtnActionPerformed

