    private void PR1_SqrRtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PR1_SqrRtActionPerformed
        // TODO add your handling code here:
         try {
            String str = PR1_InputArea_Main.getText();
            double num = Double.parseDouble(str);

            if (num >= 0) {
                double squareRoot = Math.sqrt(num);
                if (squareRoot == Math.floor(squareRoot)) {
                    PR1_OutputArea.setText(PR1_OutputArea.getText() + "The square root of " + (int)num + " is " + (int)squareRoot + "\n");
                } else {
                    PR1_OutputArea.setText(PR1_OutputArea.getText() + (int)num + " does not have a perfect integer square root.\n");
                }
            } else {
                PR1_OutputArea.setText(PR1_OutputArea.getText() + "Cannot calculate the square root of a negative number.\n");
            }
        } catch (NumberFormatException e) {
            PR1_OutputArea.setText(PR1_OutputArea.getText() + "Invalid input. Please enter a valid number.\n");
        }
    }//GEN-LAST:event_PR1_SqrRtActionPerformed

